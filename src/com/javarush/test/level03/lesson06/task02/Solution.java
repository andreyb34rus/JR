/* Таблица умножения
Выведи на экран надпись: таблицу умножения 10 на 10:
1 2 3 …
2 4 6 …
3 6 9 …
…
*/

public class Solution
{
    public static void main(String[] args)
    {

        for (int l=1; l<=10; l++) {
            System.out.print(l + " ");
        }

        System.out.println();

        for (int l = 2; l<= 20; l = l + 2) {
            System.out.print(l + " ");
        }

        System.out.println();

        for (int l = 3; l<=30; l = l + 3) {
            System.out.print(l + " ");
        }

        System.out.println();

        for (int l = 4; l<=40; l = l + 4) {
            System.out.print(l + " ");
        }

        System.out.println();

        for (int l = 5; l<= 50; l = l + 5) {
            System.out.print(l + " ");
        }

        System.out.println();

        for (int l = 6; l<=60; l = l + 6) {
            System.out.print(l + " ");
        }

        System.out.println();

        for (int l = 7; l<=70; l = l + 7) {
            System.out.print(l + " ");
        }

        System.out.println();

        for (int l = 8; l<= 80; l = l + 8) {
            System.out.print(l + " ");
        }

        System.out.println();

        for (int l = 9; l<=90; l = l + 9) {
            System.out.print(l + " ");
        }

        System.out.println();

        for (int l = 10; l<=100; l = l + 10) {
            System.out.print(l + " ");
        }

    }
}